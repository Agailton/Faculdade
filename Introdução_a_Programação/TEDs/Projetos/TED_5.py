#Faça um programa que mostre as tabuadas dos números de 1 a 10.
#Escreva o programa em Python, entregue:

for a in range(1,11):
    for b in range(1,11):

        print(f'{a} + {b} = {(a+b)}')

    print()
for c in range(1,11):
    for d in range(1,11):

        print(f'{c} - {d} = {(c-d)}')

    print()
for e in range(1,11):
    for f in range(1,11):

        print(f'{e} * {f} = {(e*f)}')

    print()

for g in range(1,11):
    for h in range(1,11):

        print(f'{g} / {h} = {(g/h)}')

    print()